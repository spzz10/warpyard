SIZE = 32
SCALE = 4  # glyph 5x7 -> 20x28
_OX, _OY = (SIZE - 5 * SCALE) // 2, (SIZE - 7 * SCALE) // 2


def _hsl_to_rgb(h: float, s: float, light: float) -> tuple[int, int, int]:
    c = (1 - abs(2 * light - 1)) * s
    x = c * (1 - abs((h / 60) % 2 - 1))
    m = light - c / 2
    r, g, b = (
        (c, x, 0.0)
        if h < 60
        else (x, c, 0.0)
        if h < 120
        else (0.0, c, x)
        if h < 180
        else (0.0, x, c)
        if h < 240
        else (x, 0.0, c)
        if h < 300
        else (c, 0.0, x)
    )
    return (round((r + m) * 255), round((g + m) * 255), round((b + m) * 255))


def _png(rows: list[bytes]) -> bytes:
    def chunk(tag: bytes, data: bytes) -> bytes:
        return struct.pack(">I", len(data)) + tag + data + struct.pack(">I", zlib.crc32(tag + data))

    ihdr = struct.pack(">IIBBBBB", SIZE, SIZE, 8, 2, 0, 0, 0)  # 8-bit RGB
    raw = b"".join(b"\x00" + r for r in rows)  # filter 0 per scanline
    return b"\x89PNG\r\n\x1a\n" + chunk(b"IHDR", ihdr) + chunk(b"IDAT", zlib.compress(raw, 9)) + chunk(b"IEND", b"")


def monogram_png(label: str) -> bytes:
    """32x32 PNG: white first-letter glyph on a label-hashed background hue."""
    label = (label or "w").lower()
    ch = label[0].upper()
    glyph = _GLYPHS.get(ch, _GLYPHS["W"])
    hue = int(hashlib.md5(label.encode(), usedforsecurity=False).hexdigest()[:8], 16) % 360
    bg = _hsl_to_rgb(hue, 0.55, 0.46)
    bg_lo = _hsl_to_rgb(hue, 0.55, 0.38)  # subtle vertical shade
    fg = (255, 255, 255)

    rows = []
    for y in range(SIZE):
        t = y / (SIZE - 1)
        base = tuple(round(bg[i] * (1 - t) + bg_lo[i] * t) for i in range(3))
        row = bytearray()
        gy = (y - _OY) // SCALE
        for x in range(SIZE):
            gx = (x - _OX) // SCALE
            lit = 0 <= gy < 7 and 0 <= gx < 5 and glyph[gy][gx] == "#"
            row += bytes(fg if lit else base)
        rows.append(bytes(row))
    return _png(rows)
